public class Add {
    public static void main(String[] args) {
        int a = Integer.parseInt(args[0]);
        System.out.println("A : " + a);
        int b = Integer.parseInt(args[1]);
        System.out.println("B: " + b);
        int c = a + b;
        System.out.println("C = " + c);
    }
}
